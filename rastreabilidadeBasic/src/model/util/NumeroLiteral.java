package model.util;

public class NumeroLiteral {
	
	public static String getNumeroLiteral(Integer numero){
		switch (numero) {
		case 1:
			return "Um";
		case 2:
			return "Dois";
		case 3:
			return "Três";
		case 4:
			return "Quatro";
		case 5:
			return "Cinco";
		case 6:
			return "Seis";
		case 7:
			return "Sete";
		case 8:
			return "Oito";
		case 9:
			return "Nove";
		case 10:
			return "Dez";
		case 11:
			return "Onze";
		case 12:
			return "Doze";
		case 13:
			return "Treze";
		case 14:
			return "Quatorze";
		case 15:
			return "Quinze";
		case 16:
			return "Dezesseis";
		case 17:
			return "Dezessete";
		case 18:
			return "Dezoito";
		case 19:
			return "Dezenove";
		case 20:
			return "Vinte";
		case 21:
			return "Vinte e Um";
		case 22:
			return "Vinte e Dois";
		case 23:
			return "Vinte e Três";
		case 24:
			return "Vinte e Quatro";
		case 25:
			return "Vinte e Cinco";
		case 26:
			return "Vinte e Seis";
		case 27:
			return "Vinte e Sete";
		case 28:
			return "Vinte e Oito";
		case 29:
			return "Vinte e Nove";
		case 30:
			return "Trinta";	
		case 31:
			return "Trinta e Um";	
		default:
			break;
		}
		
		
		return null;
		
	}
}
